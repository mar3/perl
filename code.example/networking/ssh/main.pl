#!/usr/bin/env perl
# coding: utf-8

use strict;
use POSIX;
use Net::SSH::Perl;




###############################################################################
###############################################################################
###############################################################################
###############################################################################
###############################################################################
###############################################################################
package out;

sub println {

	print(@_, "\n");
}








###############################################################################
###############################################################################
###############################################################################
###############################################################################
###############################################################################
###############################################################################
package main;

sub _main {

	POSIX::setlocale(POSIX::LC_ALL, 'C');

	my $ssh = Net::SSH::Perl->new('127.0.0.1', protocol => '2,1');
	$ssh->login('root','root');
	my ($stdout, $stderr, $exit) = $ssh->cmd('hostname');
	chomp($stdout);
	out::println('[', $stdout, ']');
}

main::_main();

